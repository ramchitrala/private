package model.professor;

/**
 *
 * @author Donald Mucharla
 */
public class Professor {
    
    private String firstNamePro;
    private String lastNamePro;
    private String agePro;
    private String countryPro;
    private String languagePro;
    private String bloodGroupPro;
    private String username;
    private String password;

    public String getFirstNamePro() {
        return firstNamePro;
    }

    public void setFirstNamePro(String firstNamePro) {
        this.firstNamePro = firstNamePro;
    }

    public String getLastNamePro() {
        return lastNamePro;
    }

    public void setLastNamePro(String lastNamePro) {
        this.lastNamePro = lastNamePro;
    }

    public String getAgePro() {
        return agePro;
    }

    public void setAgePro(String agePro) {
        this.agePro = agePro;
    }

    public String getCountryPro() {
        return countryPro;
    }

    public void setCountryPro(String countryPro) {
        this.countryPro = countryPro;
    }

    public String getLanguagePro() {
        return languagePro;
    }

    public void setLanguagePro(String languagePro) {
        this.languagePro = languagePro;
    }

    public String getBloodGroupPro() {
        return bloodGroupPro;
    }

    public void setBloodGroupPro(String bloodGroupPro) {
        this.bloodGroupPro = bloodGroupPro;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
    
}
