    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRowIndex = courseTable.getSelectedRow();
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Plese select the row to delete");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) courseTable.getModel();
        Courses selectedcouCourses =(Courses) model.getValueAt(selectedRowIndex,0);
        txtcourseTitle.setText(String.valueOf(selectedcouCourses.getCourseTitle()));
        txtsubjectcode.setText(String.valueOf(selectedcouCourses.getSubjectCode()));
        txtTerm.setText(String.valueOf(selectedcouCourses.getTerm()) );
        txtclassTime.setText(String.valueOf(selectedcouCourses.getClassTime()));
        txtcredits.setText(String.valueOf(selectedcouCourses.getNumofCredit()));
        txtModules.setText(String.valueOf(selectedcouCourses.getNumofModules()));
        txtLanguage.setText(String.valueOf(selectedcouCourses.getTeachingLanguage()));
        
        Edit.setVisible(true);
        ProfessorHomepage.setVisible(false);
    }//GEN-LAST:event_jButton2ActionPerformed

