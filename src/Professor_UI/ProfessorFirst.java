    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        String username = ProfessorunameL.getText();
        String password = ProfessorpasswordL.getText();
        ProfessorAccount professor = professorDirectory.addProfessorAccount();
        professor.setLoginusername(username);
        professor.setLoginpassword(password);
        for(ProfessorAccount p : professorDirectory.getProfessorList()){
            if(p.getUsername().equals(username) && p.getPassword().equals(password) ){
                   ProfessorLogin.setVisible(false);
                   ProfessorHomepage.setVisible(true);
                   populate();
                   JOptionPane.showMessageDialog(this, "Loged in successfully");
                   return;

            }
        }
        JOptionPane.showMessageDialog(this, "Loged failed");
    }//GEN-LAST:event_jToggleButton1ActionPerformed

